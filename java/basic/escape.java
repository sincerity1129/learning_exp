package learning_exp.java.basic;

public class escape {
    public static void main(String[] args) {
        // Same alignment
        System.out.println(
            "사과:\t1000원\n"+
            "배:\t2000원\n"+
            "귤:\t3000원\n"
            );
        // inner "" -> \\ distinction
        System.out.println("사람\"사람\"");
        // char use
        char A = '\'';
        System.out.println(A);
    }
}
